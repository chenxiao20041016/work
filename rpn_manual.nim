nbText: """
## 用法示例

RPN 计算器的核心思想是"操作数在前，操作符在后"。

### 1. 基础四则运算

**加法 (`+`)**
> 5 8 +
结果: 13

**减法 (`-`)**
> 10 4.5 -
结果: 5.5

**乘法 (`*`)**
> 6 7 *
结果: 42

**除法 (`/`)**
> 100 25 /
结果: 4

### 2. 连续运算

表达式会从左到右依次计算。

示例: 计算 (10 + 2) * 3
> 10 2 + 3 *
结果: 36
计算过程: 10 2 + 得到 12，然后 12 3 * 得到 36。

示例: 计算 (5 + 3) / (1 + 1)
> 5 3 + 1 1 + /
结果: 4
计算过程: 5 3 + 得到 8，1 1 + 得到 2，最后 8 2 / 得到 4。

### 3. 负数和浮点数运算

计算器完全支持负数和浮点数。

示例:
> -10 5.5 +
结果: -4.5

### 4. 错误处理

当输入不符合规则时，计算器会打印错误信息。

除零错误:
> 10 0 /
错误：检测到除零操作。 在处理 '/' 时发生。

操作数不足:
> 5 *
错误：栈为空，无法执行操作。 在处理 '*' 时发生。
"""

